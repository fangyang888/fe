/**
 * 数据库初始化脚本
 * 读取 public/history.txt 并导入到 MySQL
 *
 * 使用方式: npx ts-node scripts/init-db.ts
 */
import * as dotenv from 'dotenv';
import { join } from 'path';
import { readFileSync } from 'fs';
import { createConnection } from 'typeorm';
import { History } from '../src/history/history.entity';

dotenv.config({ path: join(__dirname, '..', '.env') });

async function initDb() {
  console.log('🔌 连接 MySQL...');

  const connection = await createConnection({
    type: 'mysql',
    host: process.env.DB_HOST || 'localhost',
    port: parseInt(process.env.DB_PORT || '3306', 10),
    username: process.env.DB_USER || 'root',
    password: process.env.DB_PASSWORD || '',
    database: process.env.DB_NAME || 'fe_prediction',
    entities: [History],
    synchronize: true,
  });

  console.log('✅ 数据库连接成功');

  const historyRepo = connection.getRepository(History);

  // 检查是否已有数据
  const count = await historyRepo.count();
  if (count > 0) {
    console.log(`⚠️  数据库已有 ${count} 条记录，跳过导入。`);
    console.log('   如需重新导入，请先清空 history 表。');
    await connection.close();
    return;
  }

  // 读取 history.txt
  const filePath = join(__dirname, '..', '..', 'public', 'history.txt');
  console.log(`📖 读取文件: ${filePath}`);

  const content = readFileSync(filePath, 'utf-8');
  const rows = content
    .trim()
    .split('\n')
    .filter((line) => line.trim())
    .map((line) =>
      line.split(',').map((n) => parseInt(n.trim(), 10)),
    )
    .filter((row) => row.length === 7 && row.every((n) => !isNaN(n)));

  console.log(`📊 解析到 ${rows.length} 行数据`);

  // 批量插入
  const records = rows.map((row) => {
    const record = new History();
    record.n1 = row[0];
    record.n2 = row[1];
    record.n3 = row[2];
    record.n4 = row[3];
    record.n5 = row[4];
    record.n6 = row[5];
    record.n7 = row[6];
    return record;
  });

  await historyRepo.save(records);
  console.log(`✅ 成功导入 ${records.length} 条记录到 MySQL`);

  await connection.close();
  console.log('🔒 数据库连接已关闭');
}

initDb().catch((err) => {
  console.error('❌ 初始化失败:', err.message);
  process.exit(1);
});



